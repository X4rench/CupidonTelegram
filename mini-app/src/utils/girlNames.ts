// ═══════════════════════════════════════════════════════════════
// Пул русских женских имён для AI-симуляции.
// Используется когда у юзера несколько активных диалогов с одним
// типажом — даём каждой девушке имя, чтобы их можно было различить
// в списке диалогов вместо «Стервозная 1 / Стервозная 2».
// ═══════════════════════════════════════════════════════════════

export const GIRL_NAMES: string[] = [
  // Современные популярные
  'Аня', 'Алиса', 'Вика', 'Даша', 'Катя', 'Кира', 'Лера', 'Лиза', 'Маша',
  'Настя', 'Полина', 'Соня', 'Юля',
  // Чуть более «зрелые»
  'Алёна', 'Варя', 'Ира', 'Карина', 'Кристина', 'Марина', 'Оля', 'Света',
  // Романтичные / творческие
  'Алина', 'Арина', 'Влада', 'Диана', 'Ева', 'Мила', 'Мира', 'Ника',
  'Рита', 'Стася', 'Таня',
  // Чуть необычные
  'Альбина', 'Аделина', 'Альбина', 'Ангелина', 'Богдана', 'Веста',
  'Злата', 'Лада', 'Мира', 'Олеся', 'Регина', 'Снежана', 'Тая',
  // Иностранные но привычные
  'Сабина', 'Эмма', 'Лана', 'Ясмин',
];

/**
 * Подобрать имя для новой девушки. Исключает те, что уже использованы
 * у активных сессий этого юзера. Если все 50+ уже заняты — добавляем
 * порядковый номер к случайному (на тысячи сессий не рассчитываем).
 */
export function pickGirlName(usedNames: string[]): string {
  const used = new Set(usedNames.map(n => n.trim().toLowerCase()));
  const available = GIRL_NAMES.filter(n => !used.has(n.toLowerCase()));
  if (available.length > 0) {
    // Псевдослучайно (через Date.now) — без зависимости от Math.random
    const idx = Date.now() % available.length;
    return available[idx];
  }
  // Все заняты — берём случайное с порядковым суффиксом
  const idx = Date.now() % GIRL_NAMES.length;
  return `${GIRL_NAMES[idx]} #${usedNames.length + 1}`;
}
