// Сколько раундов диалога нужно прежде чем типаж начинает теплеть/раскрываться.
// Используется в simulator_chat и ai_girl_chat — модель не должна давать имя/тёплый ответ
// раньше этого порога если парень не сделал ничего выдающегося. Раньше было статично "5+".
// Логика по канону: чем холоднее/закрытее типаж — тем выше порог.
export const TYPAZH_WARMUP_ROUNDS = {
  'Скромная':       4,  // тихая, но не сопротивляется — тает на тёплом подходе
  'Весёлая':        2,  // быстро открывается на любой юмор
  'Дерзкая':        7,  // тестит, не уважает мягкость — нужно много пройденных тестов
  'Загадочная':     6,  // ценит расшифровку, не торопится
  'Интеллектуалка': 5,  // нужно показать мысль, не "привет"
  'Спортивная':     3,  // прямая, быстро решает интересен/нет
  'Творческая':     4,  // оценивает необычность, не агрессивно
  'Романтичная':    3,  // открыта на искренность
  'Практичная':     4,  // нужно показать конкретику не пустоту
  'Стервозная':     8,  // максимально закрытая, проверяет рамки
  'Тусовщица':      2,  // живая, быстро в режим
  'Меланхоличная':  5,  // ценит понимание без слов — нужно его продемонстрировать
  'Альфа':          7,  // ищет того кто не прогнётся — тестит
  'Заботливая':     2,  // тёплая по умолчанию
  'Эмоциональная':  3,  // быстро меняет отношение, ловит вайб
  'Общительная':    2,
};

/**
 * Если типаж содержит несколько значений через запятую (мульти-выбор),
 * возвращаем объединённое описание.
 */
export function typazhDescFor(typazh) {
  if (!typazh) return 'обычная девушка, без ярко выраженных черт';
  const parts = String(typazh).split(',').map(s => s.trim()).filter(Boolean);
  if (!parts.length) return 'обычная девушка, без ярко выраженных черт';
  const descs = parts
    .map(t => TYPAZH_DESC[t])
    .filter(Boolean);
  if (!descs.length) return 'обычная девушка, без ярко выраженных черт';
  if (descs.length === 1) return descs[0];
  // Несколько типажей — соединяем через "+"
  return descs.join(' + ');
}

/**
 * Имя типажа (или комбинация) для промпта.
 * Например "Весёлая,Спортивная" → "Весёлая + Спортивная"
 */
export function typazhNameFor(typazh) {
  if (!typazh) return 'обычная';
  const parts = String(typazh).split(',').map(s => s.trim()).filter(Boolean);
  return parts.length ? parts.join(' + ') : 'обычная';
}

/**
 * Сколько раундов до потепления типажа.
 * Для мульти-типажа берём максимум (чем сложнее одна часть, тем больше нужно).
 * Дефолт для неизвестного типажа = 4 (нейтральная середина).
 */
export function typazhWarmupFor(typazh) {
  if (!typazh) return 4;
  const parts = String(typazh).split(',').map(s => s.trim()).filter(Boolean);
  if (!parts.length) return 4;
  const rounds = parts.map(t => TYPAZH_WARMUP_ROUNDS[t]).filter(n => Number.isFinite(n));
  if (!rounds.length) return 4;
  return Math.max(...rounds);
}
